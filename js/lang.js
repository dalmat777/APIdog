	get: function (category, variable, count) {
		if (category.indexOf(".") >= 0) {
			category = category.split(".");
			variable = category[1];
			category = category[0];
		}

		if (!Lang.data[0][category]) {
			return "%" + category + "." + variable + "%";
		}

		var result = Lang.data[0][category][variable], id = API.language;

		if (Lang.data[id] && Lang.data[id][category] && Lang.data[id][category][variable]) {
			result = Lang.data[id][category][variable];
		}

		if (count !== undefined) {
			result = $.textCase(count, result);
		}

		return result !== undefined ? result : "%" + category + "." + variable + "%";
	}
